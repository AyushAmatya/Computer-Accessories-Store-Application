    private void openMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openMenuItemActionPerformed
        // TODO add your handling code here:
        ArrayList<Product> temp = arrayProduct;
        try {
            arrayProduct.clear();
            file_name = JOptionPane.showInputDialog("Enter file company:");
            if (file_name.equals(null)){
                JOptionPane.showMessageDialog(myFrame, "please enter the file company");
            }
            else{                              
                File file=new File(file_name+".txt");    //creates a new file instance  
                FileReader fr=new FileReader(file);   //reads the file  
                BufferedReader br=new BufferedReader(fr);  //creates a buffering character input stream  
                String line;  
                
                clearTable();
                
                while((line=br.readLine())!=null)  
                {
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    model.addRow(line.split(","));
                    jTable1.setModel(model);                
                }
                fr.close();
                
                for (int i = 0; i < jTable1.getRowCount(); i++){
                    arrayProduct.add(new Product(jTable1.getValueAt(i, 0).toString(),jTable1.getValueAt(i, 2).toString(),jTable1.getValueAt(i, 4).toString(),jTable1.getValueAt(i, 5).toString(),jTable1.getValueAt(i, 3).toString(),jTable1.getValueAt(i, 1).toString()));
                }
            }
            btnSaveAs.setText("Save As");
            btnSave.setVisible(true);

        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(myFrame, "The file name you entered does not exit");
        } catch (IOException ex) {
            Logger.getLogger(computer_accessories.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_openMenuItemActionPerformed

    private void insertInTable(ArrayList<Product> arrayProduct){
        for(int i=0; i<arrayProduct.size(); i++){
            Product obj = arrayProduct.get(i);
            Object[] row = new Object[]{obj.product_id, obj.category, obj.company, obj.warranty, obj.price, obj.description};
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(row);
            jTable1.setModel(model);
        }
    }
    
    private void clearTable(){
        for (int i = jTable1.getRowCount() - 1; i >= 0; i--) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.removeRow(i);
        }
    }
    
