    private void writeFile(ArrayList<Product> list, String file_name){        
        try {
            PrintWriter writer = null;
            writer = new PrintWriter(file_name+".txt", "UTF-8");
            ArrayList<String> row = new ArrayList<String>();
            for(int i = 0; i<list.size(); i++){
                row.add(list.get(i).product_id);
                row.add(list.get(i).category);
                row.add(list.get(i).company);
                row.add(list.get(i).warranty);
                row.add(list.get(i).price);
                row.add(list.get(i).description);
                writer.write(String.join(",", row)+"\n");
                row.clear();
            }
            writer.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(computer_accessories.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(computer_accessories.class.getName()).log(Level.SEVERE, null, ex);
        }
    }  
    
