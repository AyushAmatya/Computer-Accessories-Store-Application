    private void sortByPrice(){
        for(int i = 0; i<arrayProduct.size()-1; i++){
            int minPosIndex = minimumPositionIndex(arrayProduct, i);
            Product temp = arrayProduct.get(minPosIndex);
            arrayProduct.set(minPosIndex, arrayProduct.get(i));
            arrayProduct.set(i, temp);
        }
    } 
